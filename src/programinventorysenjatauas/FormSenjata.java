    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        // format tanggal
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        String date = dFormat.format(jDateChooser1.getDate());
        
        //format keterangan
        String keterangan ="";
        if(masuk.isSelected()){
            keterangan = masuk.getText();
        } else {
            keterangan = keluar.getText();
        }
        
        //blok tambah data
        try {
            
            Connection conn = Config.getConnection();
            
            PreparedStatement stmt = conn.prepareStatement("INSERT INTO senjata (id, nama, jenis, magazine, tanggal, keterangan) VALUES (?,?,?,?,?,?)");
                        stmt.setString(1, id_field.getText());
                        stmt.setString(2, nama_field.getText());
                        stmt.setObject(3, jenis.getSelectedItem());
                        stmt.setObject(4, magazine.getSelectedItem());
                        stmt.setString(5, date);
                        stmt.setString(6, keterangan);
                        stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            ShowData();
            ClearForm();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        
        // format tanggal
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        String date = dFormat.format(jDateChooser1.getDate());
        
        //format keterangan
        String keterangan ="";
        if(masuk.isSelected()){
            keterangan = masuk.getText();
        } else {
            keterangan = keluar.getText();
        }
        try{
            Connection conn = Config.getConnection();
            PreparedStatement stmt = conn.prepareStatement("UPDATE senjata SET id=?, nama=?, jenis=?, magazine=?, tanggal=?, keterangan=? WHERE id=?");
            stmt.setString(1, id_field.getText());
            stmt.setString(2, nama_field.getText());
            stmt.setObject(3, jenis.getSelectedItem());
            stmt.setObject(4, magazine.getSelectedItem());
            stmt.setString(5, date);
            stmt.setString(6, keterangan);
            stmt.setString(7, id_field.getText());
            
            stmt.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Data Berhasil Diedit");
            ShowData();
            ClearForm();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void dataTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dataTableMouseClicked
        // TODO add your handling code here:
       try {
        int baris = dataTable.getSelectedRow();
        
        id_field.setText(tabelSenjata.getValueAt(baris, 0).toString());
        nama_field.setText(tabelSenjata.getValueAt(baris, 1).toString());
        jenis.setSelectedItem(tabelSenjata.getValueAt(baris, 2).toString());
        magazine.setSelectedItem(tabelSenjata.getValueAt(baris, 3).toString());
        Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String)tabelSenjata.getValueAt(baris, 4));
        jDateChooser1.setDate(date);
        
        String keterangan;
           keterangan = tabelSenjata.getValueAt(baris, 5).toString();
        if("Senjata Masuk".equals(keterangan)){
            masuk.setSelected(true);
        } else {
            keluar.setSelected(true);
        }
        } catch (ParseException ex) {
            Logger.getLogger(FormSenjata.class.getName()).log(Level.SEVERE, null, ex);
        }
    
        
    }//GEN-LAST:event_dataTableMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        Connection conn = Config.getConnection();
        int confirm = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data tersebut?", "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (confirm == 0){
            try {
                java.sql.PreparedStatement stmt = conn.prepareStatement("DELETE from senjata WHERE id ='" + id_field.getText() + "'");
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                ShowData();
                ClearForm();
            }catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

