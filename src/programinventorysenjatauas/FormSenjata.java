    public void ShowData(){
        tabelSenjata = new DefaultTableModel();
        tabelSenjata.addColumn("ID");
        tabelSenjata.addColumn("Nama");
        tabelSenjata.addColumn("Jenis");
        tabelSenjata.addColumn("Jumlah Magazine");
        tabelSenjata.addColumn("Tanggal");
        tabelSenjata.addColumn("Keterangan");
        dataTable.setModel(tabelSenjata);
        
        Connection conn = Config.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from senjata";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                tabelSenjata.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("nama"),
                    res.getString("jenis"),
                    res.getString("magazine"),
                    res.getString("tanggal"),
                    res.getString("keterangan")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
